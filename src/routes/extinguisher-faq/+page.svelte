<div class="faq">
    <h2>FAQ</h2>

    <div class="faq-item">
        <h3>What are the four types of fire extinguishers?</h3>
        <p>The four primary types of fire extinguishers are Water, Foam, Dry Powder, and CO2. Each type is designed to tackle different classes of fire.</p>
    </div>

    <div class="faq-item">
        <h3>What type of fire extinguisher is best for home use?</h3>
        <p>An ABC fire extinguisher is typically recommended for home use because it can handle fires involving ordinary combustibles, flammable liquids, and electrical equipment.</p>
    </div>

    <div class="faq-item">
        <h3>What is a fire extinguisher?</h3>
        <p>A fire extinguisher is a portable device that discharges a jet of water, foam, gas, or other substances to extinguish a fire.</p>
    </div>

    <div class="faq-item">
        <h3>What are the 7 types of fire extinguishers?</h3>
        <p>The seven types of fire extinguishers include Water, Foam, Dry Powder, CO2, Wet Chemical, Clean Agent, and Water Mist.</p>
    </div>

    <div class="faq-item">
        <h3>What does ABC mean in fire extinguisher types?</h3>
        <p>ABC refers to the types of fires the extinguisher can handle: A (ordinary combustibles like wood and paper), B (flammable liquids like oil and gasoline), and C (electrical equipment).</p>
    </div>

    <div class="faq-item">
        <h3>How to choose a fire extinguisher?</h3>
        <p>Choose a fire extinguisher based on the type of fire you are most likely to encounter. For general home use, an ABC fire extinguisher is usually the best choice.</p>
    </div>

    <div class="faq-item">
        <h3>Is an ABC fire extinguisher okay for kitchen use?</h3>
        <p>Yes, an ABC fire extinguisher can be used in the kitchen, but a wet chemical extinguisher is specifically designed for cooking oil and grease fires.</p>
    </div>

    <div class="faq-item">
        <h3>What is the best indoor fire extinguisher?</h3>
        <p>The best indoor fire extinguisher is typically an ABC fire extinguisher, as it covers a wide range of fire types commonly found indoors.</p>
    </div>

    <div class="faq-item">
        <h3>Which is better, ABC or CO2 fire extinguisher?</h3>
        <p>It depends on the situation. ABC extinguishers are versatile and can handle various fires, while CO2 extinguishers are excellent for electrical fires and leave no residue.</p>
    </div>

    <div class="faq-item">
        <h3>What is the golden rule of fire extinguishers?</h3>
        <p>The golden rule is to use the correct type of extinguisher for the fire. Always aim at the base of the fire and remember the acronym PASS: Pull, Aim, Squeeze, and Sweep.</p>
    </div>

    <div class="faq-item">
        <h3>What is the most commonly used fire extinguisher?</h3>
        <p>The most commonly used fire extinguisher is the ABC fire extinguisher, as it is versatile and can tackle most types of fires.</p>
    </div>

    <div class="faq-item">
        <h3>Is a foam or powder fire extinguisher better?</h3>
        <p>Foam extinguishers are better for liquid fires and have some cooling effect, while powder extinguishers are more versatile but can be messy and reduce visibility.</p>
    </div>

    <div class="faq-item">
        <h3>What is the best fire extinguisher for home use?</h3>
        <p>The best fire extinguisher for home use is typically an ABC fire extinguisher because it can handle most types of fires that may occur in a home.</p>
    </div>

    <div class="faq-item">
        <h3>What is the best all-around fire extinguisher for a home?</h3>
        <p>An ABC fire extinguisher is considered the best all-around option for a home due to its versatility in handling different types of fires.</p>
    </div>

    <div class="faq-item">
        <h3>Is a BC fire extinguisher better than an ABC?</h3>
        <p>A BC fire extinguisher is not necessarily better but is more specialized. It is ideal for flammable liquids and electrical fires, while an ABC extinguisher covers a broader range of fire types.</p>
    </div>
